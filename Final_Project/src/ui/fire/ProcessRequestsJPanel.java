    private void BtnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAssignActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblRequest.getSelectedRow();
        if (selectedRow < 0) return;

        WorkRequest req = (WorkRequest) tblRequest.getValueAt(selectedRow, 0);
        req.setReceiver(account);
        req.setStatus("Assigned");

        populateTable();
    }//GEN-LAST:event_BtnAssignActionPerformed

    private void BtnInProgressActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnInProgressActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblRequest.getSelectedRow();
        if (selectedRow < 0) return;

        WorkRequest req = (WorkRequest) tblRequest.getValueAt(selectedRow, 0);

        if (req.getReceiver() != account) {
            javax.swing.JOptionPane.showMessageDialog(null, "Assign to yourself first");
            return;
        }

        req.setStatus("In Progress");
        populateTable();
    }//GEN-LAST:event_BtnInProgressActionPerformed

    private void btnResolvedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResolvedActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblRequest.getSelectedRow();
        if (selectedRow < 0) return;

        WorkRequest req = (WorkRequest) tblRequest.getValueAt(selectedRow, 0);

        if (req.getReceiver() != account) {
            javax.swing.JOptionPane.showMessageDialog(null, "Assign to yourself first");
            return;
        }

        req.setMessage(req.getMessage() + " | Action: " + txtNotes.getText());
        req.setStatus("Resolved");
        req.setResolvedDate(new Date());

        populateTable();
    }//GEN-LAST:event_btnResolvedActionPerformed


