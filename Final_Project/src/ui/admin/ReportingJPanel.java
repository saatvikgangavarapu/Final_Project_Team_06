    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        model.WorkRequest selectedRequest = getSelectedRequest();
        if (selectedRequest == null) {
            return;
        }

        ReportJPanel panel = new ReportJPanel(userProcessContainer, account, selectedRequest, ReportJPanel.MODE_VIEW);
        userProcessContainer.add("ReportJPanel_VIEW", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnViewActionPerformed

