    private void setupTable() {
        tblReports.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {},
            new String [] {
                "Request ID",
                "Type",
                "Sender",
                "Status",
                "Priority",
                "Organization"
            }
        ));
        }

        private model.WorkRequest getSelectedRequest() {
            int selectedRow = tblReports.getSelectedRow();

            if (selectedRow < 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select a request first.");
                return null;
            }

            String requestId = String.valueOf(tblReports.getValueAt(selectedRow, 0));

            for (model.enterprise.Enterprise enterprise : system.getNetwork().getEnterpriseList()) {
                for (model.organization.Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    for (model.WorkRequest request : org.getWorkQueue().getWorkRequestList()) {
                        if (requestId.equals(request.getRequestId())) {
                            return request;
                        }
                    }
                }
        }

        return null;
    }
    public void refreshTable() {
        populateSummary();
        populateTable();
    }
    private void populateSummary() {
        int total = 0;
        int pending = 0;
        int inProgress = 0;
        int completed = 0;

        for (model.enterprise.Enterprise enterprise : system.getNetwork().getEnterpriseList()) {
            for (model.organization.Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                for (model.WorkRequest request : org.getWorkQueue().getWorkRequestList()) {
                    total++;

                    if ("Pending".equalsIgnoreCase(request.getStatus())) {
                        pending++;
                    } else if ("In Progress".equalsIgnoreCase(request.getStatus())) {
                        inProgress++;
                    } else if ("Completed".equalsIgnoreCase(request.getStatus())) {
                        completed++;
                    }
                }
            }
        }

        lblTotalRequestsValue.setText(String.valueOf(total));
        lblPendingValue.setText(String.valueOf(pending));
        lblInProgressValue.setText(String.valueOf(inProgress));
        lblCompletedValue.setText(String.valueOf(completed));
    }
    
    private void populateTable() {
        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) tblReports.getModel();

    model.setRowCount(0);

    for (model.enterprise.Enterprise enterprise : system.getNetwork().getEnterpriseList()) {
        for (model.organization.Organization org :
                enterprise.getOrganizationDirectory().getOrganizationList()) {

            for (model.WorkRequest request :
                    org.getWorkQueue().getWorkRequestList()) {

                Object[] row = new Object[6];

                row[0] = request.getRequestId();
                row[1] = request.getClass().getSimpleName();
                row[2] = request.getSender() != null
                        ? request.getSender().getUsername()
                        : "";

                row[3] = request.getStatus();
                row[4] = request.getPriority();
                row[5] = org.getName();

                model.addRow(row);
            }
        }
        }
    }

