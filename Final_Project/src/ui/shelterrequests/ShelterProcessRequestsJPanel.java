    private void populateFields() {
        if (request == null) {
            txtRequestId.setText("");
            txtSender.setText("");
            txtMessage.setText("");
            txtPriority.setText("");
            txtStatus.setText("");
            return;
        }

        txtRequestId.setText(request.getRequestId());
        txtSender.setText(request.getSender() != null ? request.getSender().getUsername() : "");
        txtMessage.setText(request.getMessage());
        txtPriority.setText(request.getPriority());
        txtStatus.setText(request.getStatus());
    }
