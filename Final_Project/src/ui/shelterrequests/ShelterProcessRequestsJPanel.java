    private void btnUpdateStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateStatusActionPerformed
        // TODO add your handling code here:
        if (request == null) {
            JOptionPane.showMessageDialog(this, "No request selected.");
            return;
        }

        Object selected = cmbNewStatus.getSelectedItem();
        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Please select a status.");
            return;
        }

        String newStatus = selected.toString();
        request.setStatus(newStatus);
        txtStatus.setText(newStatus);

        JOptionPane.showMessageDialog(this, "Status updated successfully.");

    }//GEN-LAST:event_btnUpdateStatusActionPerformed

    private void btnMarkCompleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarkCompleteActionPerformed
        // TODO add your handling code here:
        if (request == null) {
            JOptionPane.showMessageDialog(this, "No request selected.");
            return;
        }

        request.setStatus("Completed");
        request.setReceiver(account);
        request.setResolvedDate(new Date());
        txtStatus.setText("Completed");

        JOptionPane.showMessageDialog(this, "Request marked as completed.");
    }//GEN-LAST:event_btnMarkCompleteActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed


